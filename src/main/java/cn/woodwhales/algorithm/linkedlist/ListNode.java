package cn.woodwhales.algorithm.linkedlist;

/**
 * 链表节点对象
 * @author woodwhales
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}